-module(ex110).
-author("Yohan").

%% API
-export([a/2, f/1, g/1, h/1, k/1]).


a(_, 0) -> 0;
a(_, 1) -> 2;
a(0, Y) -> 2 * Y;
a(X, Y) -> a(X - 1, a(X, Y - 1)).

f(N) -> a(0, N).

g(N) -> a(1, N).

h(N) -> a(2, N).

k(N) -> 5 * N * N.
